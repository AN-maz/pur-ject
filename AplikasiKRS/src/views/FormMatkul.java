    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode MK");
        model.addColumn("Nama Mata Kuliah");
        model.addColumn("SKS");
        model.addColumn("Semester");
        model.addColumn("NIP Pengajar");

        table_matkul.setModel(model);

        try {
            Connection conn = KoneksiDatabase.dB_koneksi();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM mata_kuliah");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("kode_mk"),
                    rs.getString("nama_mk"),
                    rs.getString("sks"),
                    rs.getString("semester"),
                    rs.getString("nip_pengajar")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Menampilkan Data Matkul: " + e.getMessage());
        }
    }

    private void loadDosen() {
        try {
            Connection conn = KoneksiDatabase.dB_koneksi();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT nip, nama FROM dosen");

            cb_nipPengajar.removeAllItems();
            cb_nipPengajar.addItem("-- Pilih Dosen Pengajar --");

            while (rs.next()) {

                cb_nipPengajar.addItem(rs.getString("nip") + " - " + rs.getString("nama"));
            }
        } catch (Exception e) {
            System.err.println("Gagal load dosen: " + e.getMessage());
        }
    }

