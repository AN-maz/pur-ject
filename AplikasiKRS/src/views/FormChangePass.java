    private void loadUsernames() {
        try {
            Connection conn = KoneksiDatabase.dB_koneksi();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT username FROM user");

            cbb_username.removeAllItems();
            cbb_username.addItem("-- Pilih Username --");

            while (rs.next()) {
                cbb_username.addItem(rs.getString("username"));
            }

            rs.close();
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat daftar user: " + e.getMessage());
        }
    }

