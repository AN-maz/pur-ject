    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:

        if (cbb_username.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Harap pilih username terlebih dahulu!");
            return;
        }

        String username = cbb_username.getSelectedItem().toString();
        String passBaru = String.valueOf(pf_passBaru.getPassword());
        String konfirmasi = String.valueOf(pf_konfirmasi.getPassword());

        if (passBaru.isEmpty() || konfirmasi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harap isi Password Baru dan Konfirmasi!");
            return;
        }

        if (!passBaru.equals(konfirmasi)) {
            JOptionPane.showMessageDialog(this, "Password Baru dan Konfirmasi tidak cocok!");
            pf_passBaru.setText("");
            pf_konfirmasi.setText("");
            pf_passBaru.requestFocus();
            return;
        }

        try {
            Connection conn = KoneksiDatabase.dB_koneksi();
            
            String sqlUpdate = "UPDATE user SET password = ? WHERE username = ?";
            PreparedStatement pstUpdate = conn.prepareStatement(sqlUpdate);
            pstUpdate.setString(1, passBaru);
            pstUpdate.setString(2, username);

            int berhasil = pstUpdate.executeUpdate();

            if (berhasil > 0) {
                JOptionPane.showMessageDialog(this, "Password untuk user '" + username + "' berhasil diperbarui!");
                btn_bersihkanActionPerformed(evt); 
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal meriset password: " + e.getMessage());
        }

    }//GEN-LAST:event_btn_simpanActionPerformed

