    private void cb_nimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_nimActionPerformed
        // TODO add your handling code here:

        if (cb_nim.getSelectedIndex() > 0) {
            try {
                String nimDipilih = cb_nim.getSelectedItem().toString();
                Connection conn = KoneksiDatabase.dB_koneksi();

                String sqlMhs = "SELECT nama, semester_aktif FROM mahasiswa WHERE nim = ?";
                PreparedStatement pstMhs = conn.prepareStatement(sqlMhs);
                pstMhs.setString(1, nimDipilih);
                ResultSet rsMhs = pstMhs.executeQuery();

                int smtAktif = 1;
                if (rsMhs.next()) {
                    tf_nama.setText(rsMhs.getString("nama"));
                    smtAktif = rsMhs.getInt("semester_aktif");
                    tf_semester.setText(String.valueOf(smtAktif)); 

                    tampilMataKuliah(smtAktif);
                }

                String sqlIP = "SELECT AVG(nilai_angka) as ipk FROM nilai WHERE nim = ?";
                PreparedStatement pstIP = conn.prepareStatement(sqlIP);
                pstIP.setString(1, nimDipilih);
                ResultSet rsIP = pstIP.executeQuery();

                int batasMatkul = 7; 
                batasSks = 21; 

                if (rsIP.next() && rsIP.getString("ipk") != null) {
                    double ipk = rsIP.getDouble("ipk");
                    if (ipk > 3.5) {
                        batasSks = 24;
                        batasMatkul = 8; 
                    }
                }

                modelDraft.setRowCount(0); 
                totalSksSaatIni = 0;

                String sqlPaket = "SELECT kode_mk, nama_mk, sks FROM mata_kuliah WHERE semester = ?";
                PreparedStatement pstPaket = conn.prepareStatement(sqlPaket);
                pstPaket.setInt(1, smtAktif);
                ResultSet rsPaket = pstPaket.executeQuery();

                while (rsPaket.next()) {
                    String kode = rsPaket.getString("kode_mk");
                    String nama = rsPaket.getString("nama_mk");
                    int sks = rsPaket.getInt("sks");

                    modelDraft.addRow(new Object[]{kode, nama, sks});
                    totalSksSaatIni += sks;
                }

                int matkulSaatIni = modelDraft.getRowCount();
                tf_infoBatas.setText("SKS: " + totalSksSaatIni + "/" + batasSks + " | Matkul: " + matkulSaatIni + "/" + batasMatkul);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error load data mahasiswa: " + e.getMessage());
            }
        } else {
          
            tf_nama.setText("");
            tf_semester.setText("");
            modelDraft.setRowCount(0);
            totalSksSaatIni = 0;
            tf_infoBatas.setText("");
            cb_matkul.removeAllItems();
        }
    }//GEN-LAST:event_cb_nimActionPerformed

