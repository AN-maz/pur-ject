    private void tabelDraftKrsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDraftKrsMouseClicked
        // TODO add your handling code here:
        int baris = tabelDraftKrs.getSelectedRow();

        if (baris != -1) {
            String namaMk = modelDraft.getValueAt(baris, 1).toString();

            int konfirmasi = JOptionPane.showConfirmDialog(this,
                    "Hapus mata kuliah [" + namaMk + "] dari draft?",
                    "Konfirmasi Hapus Draft", JOptionPane.YES_NO_OPTION);

            if (konfirmasi == JOptionPane.YES_OPTION) {
                int sksDihapus = Integer.parseInt(modelDraft.getValueAt(baris, 2).toString());

                totalSksSaatIni -= sksDihapus;

                modelDraft.removeRow(baris);

                int batasMatkul = (batasSks == 24) ? 8 : 7;
                tf_infoBatas.setText("SKS: " + totalSksSaatIni + "/" + batasSks + " | Matkul: " + modelDraft.getRowCount() + "/" + batasMatkul);
//                tf_infoBatas.setText("Total SKS: " + totalSksSaatIni + " / Maksimal: " + batasSks + " SKS");

                JOptionPane.showMessageDialog(this, "Mata kuliah berhasil dikeluarkan dari draft.");
            }
        }
    }//GEN-LAST:event_tabelDraftKrsMouseClicked


