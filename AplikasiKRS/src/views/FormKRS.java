    private void tampilNim() {
        try {
            Connection conn = KoneksiDatabase.dB_koneksi();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT nim FROM mahasiswa");

            cb_nim.removeAllItems();
            cb_nim.addItem("-- Pilih NIM --");

            while (rs.next()) {
                cb_nim.addItem(rs.getString("nim"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Load NIM: " + e.getMessage());
        }
    }

    private void tampilMataKuliah(int semesterAktif) {
        try {
            Connection conn = KoneksiDatabase.dB_koneksi();

            String sql = "";
            if (semesterAktif % 2 == 0) {

                sql = "SELECT kode_mk, nama_mk FROM mata_kuliah WHERE semester % 2 = 0";
            } else {
                sql = "SELECT kode_mk, nama_mk FROM mata_kuliah WHERE semester % 2 != 0";
            }

            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            cb_matkul.removeAllItems();
            cb_matkul.addItem("-- Pilih Mata Kuliah --");

            while (rs.next()) {
                cb_matkul.addItem(rs.getString("kode_mk") + " - " + rs.getString("nama_mk"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Load Matkul: " + e.getMessage());
        }
    }

