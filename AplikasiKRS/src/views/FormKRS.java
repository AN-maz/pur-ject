    private void btn_simpanKRSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanKRSActionPerformed
        // TODO add your handling code here:

        if (modelDraft.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Keranjang KRS masih kosong!");
            return;
        }

        if (tf_semester.getText().isEmpty() || cb_nim.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Pastikan NIM dan Semester Tempuh sudah diisi!");
            return;
        }

        try {
            Connection conn = KoneksiDatabase.dB_koneksi();

            String sql = "INSERT INTO krs (nim, kode_mk, semester_tempuh) VALUES (?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            String nim = cb_nim.getSelectedItem().toString();
            int semester = Integer.parseInt(tf_semester.getText());

            for (int i = 0; i < modelDraft.getRowCount(); i++) {
                String kodeMk = modelDraft.getValueAt(i, 0).toString();

                pst.setString(1, nim);
                pst.setString(2, kodeMk);
                pst.setInt(3, semester);

                pst.addBatch();
            }

            pst.executeBatch();
            JOptionPane.showMessageDialog(this, "KRS Berhasil Diajukan! Menunggu ACC Dosen.");

            modelDraft.setRowCount(0);
            totalSksSaatIni = 0;
            cb_matkul.setSelectedIndex(0);

            tf_infoBatas.setText("Total SKS: 0 / Maksimal: " + batasSks + " SKS");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Menyimpan KRS: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_simpanKRSActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (cb_matkul.getSelectedIndex() > 0 && !tf_sks.getText().isEmpty()) {
            String itemDipilih = cb_matkul.getSelectedItem().toString();
            String kodeMk = itemDipilih.split(" - ")[0];
            String namaMk = itemDipilih.split(" - ")[1];
            int sksDipilih = Integer.parseInt(tf_sks.getText());

            int batasMatkul = (batasSks == 24) ? 8 : 7;
            int matkulSaatIni = modelDraft.getRowCount();

            if (matkulSaatIni >= batasMatkul) {
                JOptionPane.showMessageDialog(this, "Maksimal matkul tercapai! (Batas: " + batasMatkul + " Mata Kuliah)");
                return;
            }

            if ((totalSksSaatIni + sksDipilih) > batasSks) {
                JOptionPane.showMessageDialog(this, "Total SKS melebihi batas maksimal (" + batasSks + " SKS)!");
                return;
            }

            for (int i = 0; i < matkulSaatIni; i++) {
                if (modelDraft.getValueAt(i, 0).toString().equals(kodeMk)) {
                    JOptionPane.showMessageDialog(this, "Mata kuliah ini sudah ada di daftar!");
                    return;
                }
            }

            modelDraft.addRow(new Object[]{kodeMk, namaMk, sksDipilih});
            totalSksSaatIni += sksDipilih;

            // Update TextField Info Batas
            tf_infoBatas.setText("SKS: " + totalSksSaatIni + "/" + batasSks + " | Matkul: " + modelDraft.getRowCount() + "/" + batasMatkul);

        } else {
            JOptionPane.showMessageDialog(this, "Pilih mata kuliah terlebih dahulu!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

